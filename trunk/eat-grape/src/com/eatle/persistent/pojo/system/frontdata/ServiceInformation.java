package com.eatle.persistent.pojo.system.frontdata;

public class ServiceInformation
{
	private Long id;

	private String serveMobile;

	private String serveTel;

	private String serveQq;

	private String serveEmail;

	public Long getId()
	{
		return id;
	}

	public void setId(Long id)
	{
		this.id = id;
	}

	public String getServeMobile()
	{
		return serveMobile;
	}

	public void setServeMobile(String serveMobile)
	{
		this.serveMobile = serveMobile;
	}

	public String getServeTel()
	{
		return serveTel;
	}

	public void setServeTel(String serveTel)
	{
		this.serveTel = serveTel;
	}

	public String getServeQq()
	{
		return serveQq;
	}

	public void setServeQq(String serveQq)
	{
		this.serveQq = serveQq;
	}

	public String getServeEmail()
	{
		return serveEmail;
	}

	public void setServeEmail(String serveEmail)
	{
		this.serveEmail = serveEmail;
	}
}